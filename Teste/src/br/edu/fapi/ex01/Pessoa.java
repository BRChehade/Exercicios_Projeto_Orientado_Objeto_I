package br.edu.fapi.ex01;

public class Pessoa {
	String nome;
	int idade;
	String raça;
	String naturalidade;
	int altura;
	
	private String getNome() {
		return nome;
	}
	private void setNome(String nome) {
		this.nome = nome;
	}
	private int getIdade() {
		return idade;
	}
	private void setIdade(int idade) {
		this.idade = idade;
	}
	private String getRaça() {
		return raça;
	}
	private void setRaça(String raça) {
		this.raça = raça;
	}
	private String getNaturalidade() {
		return naturalidade;
	}
	private void setNaturalidade(String naturalidade) {
		this.naturalidade = naturalidade;
	}
	private float getAltura() {
		return altura;
	}
	private void setAltura(int altura) {
		this.altura = altura;
	}
	

	
	
}
